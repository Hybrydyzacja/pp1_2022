temp_C = float(input("Podaj temperaturę w stopniach Celcujsza: "))
print(f"Temperatura w stopniach Celcujsza wynosi {temp_C} stopni")

temp_K = temp_C + 273.15
print(f"Temperatura w Kelvinach wynosi {temp_K} Kelvinów")

temp_F = (temp_C * 9)/5 + 32
print(f"Temperatura w stopniach Fahrenhheita wynosi {temp_F} stopni")
